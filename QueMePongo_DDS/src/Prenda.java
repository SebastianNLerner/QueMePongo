
public class Prenda {
	Tipo tipo;
	Material material;
	Color colorPrimario;
	Color colorSecundario;
	Trama trama;	
	
	Prenda(Tipo tipo,Material material,Color colorPrimario,Color colorSecundario,Trama trama){
		this.tipo = tipo;
		this.material = material;
		this.colorPrimario = colorPrimario;
		this.colorSecundario = colorSecundario;
		this.trama = trama;
	}
	
	
	

}
